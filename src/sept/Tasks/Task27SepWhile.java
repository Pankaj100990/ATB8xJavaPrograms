package sept.Tasks;

public class Task27SepWhile {
    public static void main(String[] args) {
        //Write a Java program that calculates the sum of numbers from 1 to 100 using a For loop
        int s=0;
        for(int i=1;i<=100;i++)
        {
            s=s+i;
        }
        System.out.println("Sum: " + s);
    }

}
